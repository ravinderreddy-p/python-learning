#  Python Arithmetic operators
# +, _, *, /, %, **, //

print("addition of numbers: {}".format(1 + 2 + 3 + 4))

print("subtraction of numbers:{}".format(100 - 10))

print("multiplication of numbers:{}".format(10 * 10))

print("division of numbers:{}".format(99 / 10))

print("modulus of numbers:{}".format(21 % 6))

print("int division of numbers:{}".format(99 // 10))

print("exponentiation of numbers:{}".format(10 ** 10))

print("verify operators order:{}".format(1 + 2 + 3 * 3))

print("verify operators order:{}".format((1 + 2 + 3) * 3))

print("avg of 3 numbers:{}".format((23+32+64)/3))

